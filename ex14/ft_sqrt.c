int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb <= 0)
		return (0);
	else if (nb == 1)
		return (1);
	while (++i < nb && i <= 46340)
	{
		if (nb == i * i)
			return (i);
	}
	return (0);
}
